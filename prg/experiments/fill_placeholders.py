#!/usr/bin/env python3
"""
prg/experiments/fill_placeholders.py
======================================
Replace ``\\ph{...}`` placeholders in paper/sections/06_experiments.tex
with actual numerical results generated by make_figures.py.

This script reads:
  data/experiments/mc_results.csv          (§6.2 filter benchmark)
  data/experiments/supervised_results.csv  (§6.3 supervised estimation)
  data/experiments/em_results.csv          (§6.4 semi-supervised EM)

and writes numerical values directly into the LaTeX source,
replacing every ``\\ph{...}`` occurrence with the computed value.

Usage
-----
    python -m prg.experiments.fill_placeholders
    python -m prg.experiments.fill_placeholders --dry-run   # preview only
"""

from __future__ import annotations

import argparse
import pathlib
import re
import sys

import numpy as np
import pandas as pd

REPO_ROOT = pathlib.Path(__file__).resolve().parents[2]  # → exactIMM/
TEX_PATH = REPO_ROOT / "paper" / "sections" / "06_experiments.tex"
DATA_DIR = REPO_ROOT / "data" / "experiments"


# ---------------------------------------------------------------------------
# Formatting helpers
# ---------------------------------------------------------------------------


def _f(val: float, decimals: int = 4) -> str:
    """Format a float; return '\\ph{?}' if NaN."""
    if not np.isfinite(val):
        return r"\ph{?}"
    return f"{val:.{decimals}f}"


def _pct(val: float, decimals: int = 1) -> str:
    """Format a fraction as percentage."""
    if not np.isfinite(val):
        return r"\ph{?}"
    return f"{val * 100:.{decimals}f}\\%"


def _sci(val: float) -> str:
    """Format as LaTeX scientific notation, e.g. $10^{-10}$."""
    if not np.isfinite(val) or val <= 0:
        return r"\ph{?}"
    exp = int(np.floor(np.log10(val)))
    return f"$10^{{{exp}}}$"


# ---------------------------------------------------------------------------
# Load data
# ---------------------------------------------------------------------------


def _load_csv(path: pathlib.Path) -> pd.DataFrame | None:
    if not path.exists():
        print(f"  SKIP (not found): {path.name}")
        return None
    df = pd.read_csv(path)
    return df


# ---------------------------------------------------------------------------
# Build replacement dict: {placeholder_text: replacement_string}
# ---------------------------------------------------------------------------


def build_replacements(data_dir: pathlib.Path = DATA_DIR) -> dict[str, str]:
    """
    Compute all placeholder→value mappings from available CSV files.

    Returns a dict mapping the exact inner text of each ``\\ph{...}``
    to its replacement LaTeX string.  Unknown placeholders are left untouched.
    """
    rep: dict[str, str] = {}

    # ==================================================================
    # §6.2  Filter benchmark
    # ==================================================================
    df = _load_csv(data_dir / "mc_results.csv")
    if df is not None:
        for c in ("rmse", "nees", "lb_pval", "jb_pval", "log_lik", "bic", "cpu_s"):
            df[c] = pd.to_numeric(df[c], errors="coerce")

        # ---- Table 2: M1 filter benchmark (N=500, 2000, 5000) --------
        for N, n_tag in [(500, "500"), (2000, "2000"), (5000, "5000")]:
            for mode, m_tag in [("h5_exact", "h5"), ("imm_general", "imm")]:
                g = df[(df["model"] == "M1") & (df["N"] == N) & (df["mode"] == mode)]
                if g.empty:
                    continue
                rmse = g["rmse"].mean()
                nees = g["nees"].mean()
                # LB-pass: fraction of runs where p-value > 0.05 (not rejected)
                lb_pass_pct = float((g["lb_pval"] > 0.05).mean() * 100)
                cpu_s = g["cpu_s"].mean()
                cpu_us = cpu_s / N * 1e6  # µs per step
                rep[f"RMSE M1 {m_tag} N{n_tag}"] = _f(rmse)
                rep[f"NEES M1 {m_tag} N{n_tag}"] = _f(nees, 3)
                rep[f"LB M1 {m_tag} N{n_tag}"] = _f(lb_pass_pct, 0)
                rep[f"CPU M1 {m_tag} N{n_tag}"] = _f(cpu_us, 1)

        # ---- RMSE gap M1 h5 vs imm at N=2000 (narrative) ---------------
        h5 = df[(df["model"] == "M1") & (df["N"] == 2000) & (df["mode"] == "h5_exact")][
            "rmse"
        ].mean()
        imm = df[(df["model"] == "M1") & (df["N"] == 2000) & (df["mode"] == "imm_general")][
            "rmse"
        ].mean()
        if np.isfinite(h5) and np.isfinite(imm) and imm > 0:
            gap_pct = (imm - h5) / imm * 100
            rep["RMSE gap h5 vs imm M1 N2000 pct"] = _f(gap_pct, 1)

        # ---- NEES direction for narrative (above/below 1) ---------------
        for mode, m_tag in [("h5_exact", "h5"), ("imm_general", "imm")]:
            g = df[(df["model"] == "M1") & (df["N"] == 2000) & (df["mode"] == mode)]
            if not g.empty:
                nees_val = g["nees"].mean()
                rep[f"NEES M1 {m_tag} N2000"] = _f(nees_val, 3)

        # ---- LB reject rate for narrative --------------------------------
        for mode, m_tag in [("h5_exact", "h5"), ("imm_general", "imm")]:
            g = df[(df["model"] == "M1") & (df["N"] == 2000) & (df["mode"] == mode)]
            if not g.empty:
                reject_pct = float((g["lb_pval"] < 0.05).mean() * 100)
                rep[f"LB reject M1 {m_tag} N2000"] = _f(reject_pct, 0)

        # ---- Table 3: M2/M3 at N=2000 --------------------------------
        for model in ("M2", "M3"):
            for mode, m_tag in [("h5_exact", "h5"), ("imm_general", "imm")]:
                g = df[(df["model"] == model) & (df["N"] == 2000) & (df["mode"] == mode)]
                if g.empty:
                    continue
                lb_pass_pct = float((g["lb_pval"] > 0.05).mean() * 100)
                rep[f"RMSE {model} {m_tag}"] = _f(g["rmse"].mean())
                rep[f"NEES {model} {m_tag}"] = _f(g["nees"].mean(), 3)
                rep[f"LB {model} {m_tag}"] = _f(lb_pass_pct, 0)

        # ---- BIC (K=2, true model, from h5_exact log-lik) ---------------
        # Note: proper BIC for K≠2 requires separate EM fits per K.
        # Here we only compute the K=2 BIC as a reference.
        from prg.experiments.metrics import dof_h5

        sub_bic = df[(df["model"] == "M1") & (df["N"] == 2000) & (df["mode"] == "h5_exact")]
        d2 = dof_h5(2, 1, 1)
        bic2 = sub_bic["log_lik"].dropna().apply(lambda ll: d2 * np.log(2000) - 2.0 * ll).mean()
        rep["BIC K2 M1 N2000"] = _f(bic2, 1)

    # ==================================================================
    # §6.3  Supervised estimation
    # ==================================================================
    df_sup = _load_csv(data_dir / "supervised_results.csv")
    if df_sup is not None:
        for c in ("rel_err_F", "rel_err_b", "h5_residual", "rmse_estimated", "rmse_oracle"):
            df_sup[c] = pd.to_numeric(df_sup[c], errors="coerce")

        for proj in ("none", "ab"):
            sub = df_sup[df_sup["projection"] == proj]
            for N in (200, 500, 1000, 2000):
                g = sub[sub["N"] == N]
                if g.empty:
                    continue
                mean_F = g["rel_err_F"].mean()
                rep[f"relF {proj} N{N}"] = _f(mean_F)
                # Relative error on bias vector (where available)
                rb = g["rel_err_b"].dropna()
                if len(rb) > 0:
                    rep[f"relb {proj} N{N}"] = _f(rb.mean())

            # H5 residual at N=2000
            g2k = sub[sub["N"] == 2000]["h5_residual"].dropna()
            if len(g2k) > 0:
                rep[f"h5resid {proj} N2000"] = _sci(float(g2k.median()))

    # ==================================================================
    # §6.4  Semi-supervised EM
    # ==================================================================
    df_em = _load_csv(data_dir / "em_results.csv")
    if df_em is not None:
        for c in ("best_log_lik", "basin_rate", "rel_err_F", "rmse_estimated", "rmse_oracle"):
            df_em[c] = pd.to_numeric(df_em[c], errors="coerce")

        for var in ("PH", "GEM"):
            sub = df_em[df_em["variant"] == var]
            for N in (500, 2000):
                g = sub[sub["N"] == N]
                if g.empty:
                    continue
                rep[f"EM basin {var} N{N}"] = _pct(g["basin_rate"].mean())
                rep[f"EM relF {var} N{N}"] = _f(g["rel_err_F"].mean())
                rep[f"EM niter {var} N{N}"] = _f(g["best_n_iter"].mean(), 1)
                rep[f"EM rmse {var} N{N}"] = _f(g["rmse_estimated"].mean())
                rep[f"EM rmse oracle N{N}"] = _f(g["rmse_oracle"].mean())

        # ---- EM basin rate as function of n_inits (post-hoc) --------
        sub_ph = df_em[(df_em["model"] == "M1") & (df_em["N"] == 2000) & (df_em["variant"] == "PH")]
        for n_init in (1, 2, 3, 5):
            rates = []
            for _, row in sub_ph.iterrows():
                raw = str(row.get("all_log_liks", ""))
                if not raw or raw == "nan":
                    continue
                all_lls = [float(x) for x in raw.split(";") if x]
                if not all_lls or not any(np.isfinite(ll) for ll in all_lls):
                    continue
                # Global best over ALL stored restarts
                global_best = max(ll for ll in all_lls if np.isfinite(ll))
                threshold = global_best - 0.01 * abs(global_best)
                # Does any of the first n_init restarts reach the global basin?
                lls = all_lls[:n_init]
                basin = any(ll >= threshold for ll in lls if np.isfinite(ll))
                rates.append(float(basin))
            if rates:
                rep[f"EM basin PH N2000 ninit{n_init}"] = _pct(float(np.mean(rates)))

    return rep


# ---------------------------------------------------------------------------
# Apply replacements to the LaTeX file
# ---------------------------------------------------------------------------

# Pattern matching  \ph{some text here}
_PH_RE = re.compile(r"\\ph\{([^}]*)\}")


def _replace_in_text(text: str, rep: dict[str, str]) -> tuple[str, int]:
    """
    Replace all ``\\ph{key}`` occurrences where key is in *rep*.
    Returns (new_text, n_replaced).
    """
    count = 0

    def _sub(m: re.Match) -> str:
        nonlocal count
        key = m.group(1).strip()
        if key in rep:
            count += 1
            return rep[key]
        return m.group(0)  # leave unchanged

    return _PH_RE.sub(_sub, text), count


def fill_placeholders(
    tex_path: pathlib.Path = TEX_PATH,
    data_dir: pathlib.Path = DATA_DIR,
    dry_run: bool = False,
    verbose: bool = True,
) -> int:
    """
    Fill ``\\ph{...}`` placeholders in *tex_path* with computed values.

    Parameters
    ----------
    tex_path  : path to 06_experiments.tex
    data_dir  : directory containing *_results.csv files
    dry_run   : if True, print replacements but do not write the file
    verbose   : print per-replacement summary

    Returns
    -------
    int  number of placeholders replaced
    """
    if not tex_path.exists():
        print(f"ERROR: LaTeX file not found: {tex_path}", file=sys.stderr)
        return 0

    print(f"Building replacements from: {data_dir}")
    rep = build_replacements(data_dir)
    print(f"  {len(rep)} computed values available.")

    text = tex_path.read_text(encoding="utf-8")

    # Count total placeholders before replacement
    total_ph = len(_PH_RE.findall(text))
    print(f"  {total_ph} \\ph{{}} occurrences in {tex_path.name}")

    if verbose:
        print("\nAvailable replacements:")
        for k, v in sorted(rep.items()):
            print(f"  \\ph{{{k}}}  →  {v}")

    new_text, n_replaced = _replace_in_text(text, rep)
    remaining = total_ph - n_replaced

    print(f"\nReplaced: {n_replaced}/{total_ph}  ({remaining} remaining as \\ph{{}})")

    if not dry_run and n_replaced > 0:
        tex_path.write_text(new_text, encoding="utf-8")
        print(f"Written: {tex_path}")
    elif dry_run:
        print("(dry-run: file not written)")

    return n_replaced


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Fill \\ph{} placeholders in 06_experiments.tex.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("--tex", default=str(TEX_PATH))
    parser.add_argument("--data-dir", default=str(DATA_DIR))
    parser.add_argument(
        "--dry-run", action="store_true", help="Preview replacements without writing the file."
    )
    parser.add_argument("--quiet", action="store_true")
    args = parser.parse_args()

    fill_placeholders(
        tex_path=pathlib.Path(args.tex),
        data_dir=pathlib.Path(args.data_dir),
        dry_run=args.dry_run,
        verbose=not args.quiet,
    )
